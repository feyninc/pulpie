class DistillDataset(Dataset):
    """Loads teacher logits lazily from disk."""

    def __init__(self, indices, meta, logits_dir):
        self.indices = indices
        self.input_ids = meta['input_ids']
        self.labels = meta['labels']
        self.logits_dir = logits_dir

    def __len__(self):
        return len(self.indices)

    def __getitem__(self, idx):
        global_idx = self.indices[idx]
        teacher_logits = torch.load(
            os.path.join(self.logits_dir, f'{global_idx}.pt'),
            weights_only=True,
        ).float()
        return {
            'input_ids': self.input_ids[global_idx],
            'labels': self.labels[global_idx],
            'teacher_logits': teacher_logits,
        }


class DistillCollator:
    def __init__(self, pad_token_id):
        self.pad_token_id = pad_token_id

    def __call__(self, features):
        max_len = max(len(f['input_ids']) for f in features)

        input_ids = []
        attention_mask = []
        labels = []
        teacher_logits = []

        for f in features:
            seq_len = len(f['input_ids'])
            pad_len = max_len - seq_len
            input_ids.append(f['input_ids'] + [self.pad_token_id] * pad_len)
            attention_mask.append([1] * seq_len + [0] * pad_len)
            labels.append(f['labels'] + [IGNORE_LABEL] * pad_len)
            tl = f['teacher_logits']  # (seq_len, 2) tensor
            if pad_len > 0:
                tl = torch.cat([tl, torch.zeros(pad_len, 2)], dim=0)
            teacher_logits.append(tl)

        return {
            'input_ids': torch.tensor(input_ids, dtype=torch.long),
            'attention_mask': torch.tensor(attention_mask, dtype=torch.long),
            'labels': torch.tensor(labels, dtype=torch.long),
            'teacher_logits': torch.stack(teacher_logits),
        }


class DistillTrainer(Trainer):
    def __init__(self, alpha=0.7, temperature=2.0, **kwargs):
        super().__init__(**kwargs)
        self.alpha = alpha
        self.temperature = temperature

    def compute_loss(self, model, inputs, return_outputs=False, **kwargs):
        labels = inputs.pop('labels')
        teacher_logits = inputs.pop('teacher_logits')
        attention_mask = inputs['attention_mask']

        outputs = model(**inputs)
        student_logits = outputs.logits

        mask = attention_mask.bool()
        T = self.temperature

        teacher_soft = F.log_softmax(teacher_logits / T, dim=-1)
        student_soft = F.log_softmax(student_logits / T, dim=-1)

        kl_per_token = F.kl_div(
            student_soft, teacher_soft, log_target=True, reduction='none'
        ).sum(dim=-1)

        kl_loss = (kl_per_token * mask.float()).sum() / mask.float().sum()
        kl_loss = kl_loss * (T * T)

        ce_loss = F.cross_entropy(
            student_logits.view(-1, student_logits.size(-1)),
            labels.view(-1),
            ignore_index=IGNORE_LABEL,
        )

        loss = self.alpha * kl_loss + (1 - self.alpha) * ce_loss
        return (loss, outputs) if return_outputs else loss


def compute_metrics(eval_pred):
    logits, labels = eval_pred
    preds = np.argmax(logits, axis=-1)
    mask = labels != IGNORE_LABEL
    flat_preds = preds[mask]
    flat_labels = labels[mask]

    acc = (flat_preds == flat_labels).mean()
    f1 = f1_score(flat_labels, flat_preds, pos_label=LABEL_MAIN, zero_division=0)
    prec = precision_score(flat_labels, flat_preds, pos_label=LABEL_MAIN, zero_division=0)
    rec = recall_score(flat_labels, flat_preds, pos_label=LABEL_MAIN, zero_division=0)

    return {'accuracy': acc, 'f1': f1, 'precision': prec, 'recall': rec}


