"""Distill EuroBERT-2.1B block classifier into EuroBERT-610m via all-position KL-div.

Phase 2: loads pre-computed teacher logits from disk (generated by generate_teacher_logits.py).

Loss = alpha * KL(teacher_soft || student_soft) + (1-alpha) * CE(student, hard_labels_at_sep)

Usage:
  # First run: python data/generate_teacher_logits.py --num-gpus 4
  # Then train:
  accelerate launch --num_processes 4 data/train_distill_eurobert.py --epochs 1
"""

